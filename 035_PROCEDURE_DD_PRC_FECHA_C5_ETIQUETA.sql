	 SET pq.SEQPALETECARREG = COALESCE(pq.SEQPALETECARREG, V_SEQPALETE),
	     pq.CODPRODUTIVO = COALESCE(pq.CODPRODUTIVO, V_CODPROD_EFETIVO),
	     pq.DTAHORAMONTAGEMPALETE = COALESCE(pq.DTAHORAMONTAGEMPALETE, V_DTA_FECHAMENTO)
       WHERE V_SEQPALETERF_DER IS NOT NULL
	 AND pq.SEQPALETERF = V_SEQPALETERF_DER
	 AND V_SEQPALETEQTDE_DER IS NOT NULL
	 AND pq.SEQPALETEQTDE = V_SEQPALETEQTDE_DER;
      INSERT INTO CD2.DD_SORTER_EVENTO_CTRL (
	ID_EVENTO, CODBARRAETQ, ORIGEM, STATUS_ORIGEM, DTA_EVENTO_ORIGEM, DTA_CAPTURA, HASH_EVENTO, OBS
      ) VALUES (
	CD2.SQ_DD_SORTER_EVENTO_CTRL.NEXTVAL,
	P_CODBARRAETQ,
	'FECHAMENTO',
	CASE
	  WHEN (NVL(V_QTD_SEP, 0) + NVL(V_QTD_CAN, 0) >= V_QTD_SOL) THEN 'C5_FECHADO'
	  ELSE 'C5_FECHADO_COM_DIVERGENCIA'
	END,
	SYSDATE,
	SYSDATE,
	P_CODBARRAETQ || '|FECHA|' ||
	  CASE
	    WHEN (NVL(V_QTD_SEP, 0) + NVL(V_QTD_CAN, 0) >= V_QTD_SOL) THEN 'C5_FECHADO'
	    ELSE 'C5_FECHADO_COM_DIVERGENCIA'
	  END || '|' || TO_CHAR(SYSDATE, 'YYYYMMDDHH24MISS'),
	CASE
	  WHEN (NVL(V_QTD_SEP, 0) + NVL(V_QTD_CAN, 0) >= V_QTD_SOL) THEN
	    'FECHAMENTO CONTROLADO COM STATUS INVENT=' || V_STATUS_SORTER || ' E CONCILIACAO=' || V_STATUS_CONC
	  ELSE
	    'FECHADO COM DIVERGENCIA QUANTITATIVA: SOL=' || TO_CHAR(V_QTD_SOL) ||
	    ' SEP=' || TO_CHAR(NVL(V_QTD_SEP, 0)) ||
	    ' CAN=' || TO_CHAR(NVL(V_QTD_CAN, 0))
	END
      );
      CD2.DD_PKG_FECHA_CACHE.DECR_QTD_PEND(V_CACHE_DEST_KEY);
      COMMIT;
    END IF;
  ELSE
    BEGIN
      SELECT MAX(e.STATUS_ORIGEM) KEEP (DENSE_RANK LAST ORDER BY e.ID_EVENTO)
	INTO V_ULT_FECHA_LOG
	FROM CD2.DD_SORTER_EVENTO_CTRL e
       WHERE e.CODBARRAETQ = P_CODBARRAETQ
	 AND e.ORIGEM = 'FECHAMENTO';
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
	V_ULT_FECHA_LOG := NULL;
    END;
    IF NVL(V_ULT_FECHA_LOG, 'X') <> 'NAO_FECHADO' THEN
      BEGIN
	INSERT INTO CD2.DD_SORTER_EVENTO_CTRL (
	  ID_EVENTO, CODBARRAETQ, ORIGEM, STATUS_ORIGEM, DTA_EVENTO_ORIGEM, DTA_CAPTURA, HASH_EVENTO, OBS
	) VALUES (
	  CD2.SQ_DD_SORTER_EVENTO_CTRL.NEXTVAL,
	  P_CODBARRAETQ,
	  'FECHAMENTO',
	  'NAO_FECHADO',
	  SYSDATE,
	  SYSDATE,
	  P_CODBARRAETQ || '|FECHA|NAO_FECHADO',
	  CASE
	    WHEN V_STATUS_SORTER NOT IN ('INDUZIDO', 'RETORNO_PRODUTO') THEN
	      'STATUS INVENT NAO ELEGIVEL: ' || V_STATUS_SORTER
	    WHEN V_QTD_SOL IS NULL THEN
	      'SEM_BASE_QUANTITATIVA (QTD_SOLICITADA NULA)'
	    WHEN NVL(V_QTD_SEP, 0) = 0 THEN
	      'QTD_SEPARADA_ZERO'
	    WHEN (NVL(V_QTD_SEP, 0) + NVL(V_QTD_CAN, 0) < V_QTD_SOL) THEN
	      'QTD_INSUFICIENTE: SOL=' || TO_CHAR(V_QTD_SOL) ||
	      ' SEP=' || TO_CHAR(NVL(V_QTD_SEP, 0)) ||
	      ' CAN=' || TO_CHAR(NVL(V_QTD_CAN, 0))
	    ELSE
	      'NAO_ELEGIVEL_POR_REGRA_QUANTITATIVA'
	  END
	);
	COMMIT;
      EXCEPTION
	WHEN DUP_VAL_ON_INDEX THEN
	  NULL;
      END;
    END IF;
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    INSERT INTO CD2.DD_SORTER_EVENTO_CTRL (
      ID_EVENTO, CODBARRAETQ, ORIGEM, STATUS_ORIGEM, DTA_EVENTO_ORIGEM, DTA_CAPTURA, HASH_EVENTO, OBS
    ) VALUES (
      CD2.SQ_DD_SORTER_EVENTO_CTRL.NEXTVAL,
      P_CODBARRAETQ,
      'FECHAMENTO',
      'SEM_DADOS',
      SYSDATE,
      SYSDATE,
      P_CODBARRAETQ || '|FECHA|SEM_DADOS|' || TO_CHAR(SYSDATE, 'YYYYMMDDHH24MISS'),
      'ETIQUETA NAO ENCONTRADA EM CORRELACAO/PENDENCIAS'
    );
    COMMIT;
END;
