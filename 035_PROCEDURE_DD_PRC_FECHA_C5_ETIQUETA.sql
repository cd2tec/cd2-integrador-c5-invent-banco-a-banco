CREATE OR REPLACE PROCEDURE "DD_PRC_FECHA_C5_ETIQUETA" (
  P_CODBARRAETQ IN VARCHAR2
) AS
  V_STATUS_SORTER  VARCHAR2(40);
  V_STATUS_CONC    VARCHAR2(40);
  V_C5_PROC_INT    VARCHAR2(1);
  V_C5_PROC_MONT   VARCHAR2(1);
  V_QTD_SOL	   NUMBER;
  V_QTD_SEP	   NUMBER;
  V_QTD_CAN	   NUMBER;
  V_NROEMPRESA	   NUMBER;
  V_TIPESPECIE	   VARCHAR2(10);
  V_PALLET_INVENT  VARCHAR2(200);
  V_SEQPALETE	   NUMBER;
  V_CODPROD_INVENT NUMBER;
  V_DTA_ABERTURA   DATE;
  V_DTA_FECHAMENTO DATE;
  V_NROCARGA	   NUMBER;
  V_SEQPRODUTO	   NUMBER;
  V_TIPOCARGA	   VARCHAR2(2);
  V_CODPROD_PADRAO NUMBER;
  V_CODPROD_EFETIVO NUMBER;
  V_TMP_COUNT	   NUMBER;
  V_DESC_PRODUTO   VARCHAR2(250);
  V_TIP_ARMAZ	   VARCHAR2(10);
  V_PESO_BRUTO	   NUMBER;
  V_MCUB	   NUMBER;
  V_PESAVEL	   VARCHAR2(1);
  V_CODRH_EFETIVO  VARCHAR2(50);
  V_NOME_PROD_EF   VARCHAR2(100);
  V_DESC_TIPESP    VARCHAR2(100);
  V_IND_BRACO	   VARCHAR2(1);
  V_IND_PALETECHK  VARCHAR2(1);
  V_SEQPALETEQTDE_DER NUMBER;
  V_SEQPALETERF_DER NUMBER;
  V_SEQCARREG_PALETE NUMBER;
  V_SEQPALETE_VOLUME NUMBER;
  V_ID_GPT_RETORNO NUMBER;
  V_SEQLOTE	   NUMBER;
  V_LOJA_DESTINO   NUMBER;
  V_QTD_PEND_DEST  NUMBER;
  V_CNT_PALLET_CAND NUMBER := 0;
  V_ULT_FECHA_LOG   VARCHAR2(40);
  V_CACHE_DEST_KEY  VARCHAR2(200);
  V_CACHE_CNT_KEY   VARCHAR2(200);
  C_USU_MONTAGEM_SORTER CONSTANT VARCHAR2(20) := 'INVENT';
BEGIN
  SELECT NVL(STATUS_SORTER, STATUS_VOLUME)
    INTO V_STATUS_SORTER
    FROM CD2.DDV_SORTER_CORRELACAO
   WHERE CODBARRAETQ = P_CODBARRAETQ
     AND ROWNUM = 1;
  SELECT STATUS_CONCILIACAO
    INTO V_STATUS_CONC
    FROM CD2.DDV_SORTER_PENDENCIAS
   WHERE CODBARRAETQ = P_CODBARRAETQ
     AND ROWNUM = 1;
  SELECT QTD_EMB_SOLICITADA, QTD_EMB_SEPARADA, QTD_EMB_CANCELADA
    INTO V_QTD_SOL, V_QTD_SEP, V_QTD_CAN
    FROM CD2.DDV_SORTER_CORRELACAO_ENRIQ
   WHERE CODBARRAETQ = P_CODBARRAETQ
     AND ROWNUM = 1;
  SELECT NVL(MAX(INDPROCESSADO), 'N')
    INTO V_C5_PROC_INT
    FROM CONSINCO.MLO_INTEGRACAOSORTER
   WHERE CODBARRAETQ = P_CODBARRAETQ;
  SELECT MAX(NROEMPRESA), MAX(TIPESPECIE)
    INTO V_NROEMPRESA, V_TIPESPECIE
    FROM CONSINCO.MLO_INTEGRACAOSORTER
   WHERE CODBARRAETQ = P_CODBARRAETQ;
  SELECT MAX(NROCARGA), MAX(SEQPRODUTO), MAX(TIPOCARGA)
    INTO V_NROCARGA, V_SEQPRODUTO, V_TIPOCARGA
    FROM CONSINCO.MLO_INTEGRACAOSORTER
   WHERE CODBARRAETQ = P_CODBARRAETQ;
  SELECT NVL(MAX(INDPROCESSADO), 'N')
    INTO V_C5_PROC_MONT
    FROM CONSINCO.MLO_MONTAGEMSORTER
   WHERE CODBARRAETQ = P_CODBARRAETQ;
  /* Recebimento (R): nao usa MLO_CARGAEPRODUTO; elegivel com retorno Invent (mesma regra do envio 021). */
  IF UPPER(TRIM(NVL(V_TIPOCARGA, 'E'))) = 'R'
     AND V_STATUS_SORTER IN ('INDUZIDO', 'RETORNO_PRODUTO') THEN
    V_QTD_SOL := NVL(V_QTD_SOL, 1);
    V_QTD_SEP := GREATEST(NVL(V_QTD_SEP, 0), 1);
  END IF;
  IF V_STATUS_SORTER IN ('INDUZIDO', 'RETORNO_PRODUTO')
     AND (
	   UPPER(TRIM(NVL(V_TIPOCARGA, 'E'))) = 'R'
	OR (V_QTD_SOL IS NOT NULL AND NVL(V_QTD_SEP, 0) > 0)
	 ) THEN
    IF REGEXP_LIKE(TRIM(NVL(V_TIPESPECIE, 'X')), '^[0-9]+$') THEN
      V_SEQLOTE := TO_NUMBER(TRIM(V_TIPESPECIE));
    ELSE
      V_SEQLOTE := ABS(MOD(DBMS_UTILITY.GET_HASH_VALUE(NVL(V_TIPESPECIE, P_CODBARRAETQ), 1, 999999), 999999));
    END IF;
    BEGIN
      SELECT TO_NUMBER(REGEXP_SUBSTR(TRIM(t.DESCTIPESPECIE), '^[0-9]+'))
	INTO V_LOJA_DESTINO
	FROM CONSINCO.MLO_TIPESPECIE t
       WHERE t.NROEMPRESA = V_NROEMPRESA
	 AND t.TIPESPECIE = V_TIPESPECIE
	 AND REGEXP_LIKE(TRIM(t.DESCTIPESPECIE), '^[0-9]+')
	 AND ROWNUM = 1;
    EXCEPTION
      WHEN OTHERS THEN
	IF REGEXP_LIKE(TRIM(NVL(V_TIPESPECIE, 'X')), '^[0-9]+$') THEN
	  V_LOJA_DESTINO := 1000 + TO_NUMBER(TRIM(V_TIPESPECIE));
	ELSE
	  V_LOJA_DESTINO := V_NROEMPRESA;
	END IF;
    END;
    V_CACHE_DEST_KEY := V_NROCARGA || '|' || V_NROEMPRESA || '|' || V_TIPESPECIE || '|' ||
			V_SEQPRODUTO || '|' || UPPER(TRIM(NVL(V_TIPOCARGA, 'E')));
    IF NOT CD2.DD_PKG_FECHA_CACHE.GET_QTD_PEND(V_CACHE_DEST_KEY, V_QTD_PEND_DEST) THEN
      SELECT COUNT(*)
	INTO V_QTD_PEND_DEST
	FROM CD2.DDV_SORTER_CORRELACAO_ENRIQ e
       WHERE e.NROCARGA = V_NROCARGA
	 AND e.NROEMPRESA = V_NROEMPRESA
	 AND e.TIPESPECIE = V_TIPESPECIE
	 AND e.SEQPRODUTO = V_SEQPRODUTO
	 AND (
	       UPPER(TRIM(NVL(V_TIPOCARGA, 'E'))) = 'R'
	    OR NVL(e.QTD_EMB_SEPARADA, 0) > 0
	     )
	 AND NOT EXISTS (
	       SELECT 1
		 FROM CONSINCO.MLO_PALETE p
		 JOIN CONSINCO.MLO_PALCARREGRF prf
		   ON prf.SEQPALETERF = p.SEQPALETERF
		WHERE p.NROCARGARECEB = V_NROCARGA
		  AND p.NROEMPRESA = V_NROEMPRESA
		  AND p.SEQPRODUTO = V_SEQPRODUTO
		  AND p.TIPESPECIE = V_TIPESPECIE
		  AND prf.SEQPALETECARREG IS NOT NULL
		  AND prf.SEQPALETEQTDE = CASE
			WHEN REGEXP_LIKE(e.CODBARRAETQ, '[0-9]{7}$')
			  THEN TO_NUMBER(REGEXP_SUBSTR(e.CODBARRAETQ, '[0-9]{7}$'))
			WHEN REGEXP_LIKE(e.CODBARRAETQ, '[0-9]{6}$')
			  THEN TO_NUMBER(REGEXP_SUBSTR(e.CODBARRAETQ, '[0-9]{6}$'))
			ELSE NULL
		      END
	     );
      CD2.DD_PKG_FECHA_CACHE.SET_QTD_PEND(V_CACHE_DEST_KEY, V_QTD_PEND_DEST);
    END IF;
    V_PALLET_INVENT := NULL;
    V_CODPROD_INVENT := NULL;
    V_DTA_ABERTURA := NULL;
    V_DTA_FECHAMENTO := NULL;
    V_ID_GPT_RETORNO := NULL;
    /* Fonte da verdade: GPT_RETORNO_MONTAGEM_PALETES@INVENT (PALLET/master + SEQPRODUTO + LOJA).
       Palete fisico pode ter varios produtos; cada linha GPT (ID) vincula 1 etiqueta (volume). */
    BEGIN
      SELECT v.PALLET_INVENT, v.SEQPALETECARREG
	INTO V_PALLET_INVENT, V_SEQPALETE
	FROM CD2.DD_SORTER_VINCULO_RETORNO_ETQ v
       WHERE v.CODBARRAETQ = P_CODBARRAETQ;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
	V_PALLET_INVENT := NULL;
	V_SEQPALETE := NULL;
      WHEN OTHERS THEN
	V_PALLET_INVENT := NULL;
	V_SEQPALETE := NULL;
    END;
    IF V_PALLET_INVENT IS NULL AND NVL(V_QTD_PEND_DEST, 0) > 0 THEN
      BEGIN
	EXECUTE IMMEDIATE '
	  SELECT PALLET, COD_PRODUTIVO, DATA_ABERTURA, DATA_FECHAMENTO,
		 ID_GPT_RETORNO_MONTAGEM_PALETES
	    FROM (
	      SELECT r.PALLET,
			     r.COD_PRODUTIVO,
			     r.DATA_ABERTURA,
			     r.DATA_FECHAMENTO,
			     r.ID_GPT_RETORNO_MONTAGEM_PALETES
			FROM "GPT_RETORNO_MONTAGEM_PALETES"@INVENT r
		       WHERE r.EMPRESA = :1
			 AND r.ONDA = :2
			 AND r.SEQPRODUTO = :3
			 AND r.LOJA = :4
			 AND r.LOJA <> r.EMPRESA
			 AND r.DATA_FECHAMENTO IS NOT NULL
			 AND NOT EXISTS (
			       SELECT 1
				 FROM CD2.DD_SORTER_VINCULO_RETORNO_ETQ v
				WHERE v.ID_GPT_RETORNO = r.ID_GPT_RETORNO_MONTAGEM_PALETES
			      )
		      ORDER BY r.DATA_FECHAMENTO DESC, r.ID_GPT_RETORNO_MONTAGEM_PALETES
	    )
	   WHERE ROWNUM = 1'
	  INTO V_PALLET_INVENT, V_CODPROD_INVENT, V_DTA_ABERTURA, V_DTA_FECHAMENTO, V_ID_GPT_RETORNO
	  USING V_NROEMPRESA, V_NROCARGA, V_SEQPRODUTO, V_LOJA_DESTINO;
      EXCEPTION
	WHEN NO_DATA_FOUND THEN
	  V_PALLET_INVENT := NULL;
	WHEN OTHERS THEN
	  V_PALLET_INVENT := NULL;
      END;
      IF V_PALLET_INVENT IS NULL THEN
	V_CACHE_CNT_KEY := V_NROEMPRESA || '|' || V_NROCARGA || '|' || V_SEQPRODUTO ||
			   '|EMP|' || TO_CHAR(NVL(V_QTD_PEND_DEST, 0));
	IF NOT CD2.DD_PKG_FECHA_CACHE.GET_CNT_PALLET(V_CACHE_CNT_KEY, V_CNT_PALLET_CAND) THEN
	  BEGIN
	    EXECUTE IMMEDIATE '
	      SELECT COUNT(DISTINCT px.PALLET)
		FROM (
		       SELECT r.PALLET
			 FROM "GPT_RETORNO_MONTAGEM_PALETES"@INVENT r
			WHERE r.EMPRESA = :1
			  AND r.ONDA = :2
			  AND r.SEQPRODUTO = :3
			  AND r.DATA_FECHAMENTO IS NOT NULL
			  AND r.LOJA = r.EMPRESA
			  AND NOT EXISTS (
				SELECT 1
				  FROM CD2.DD_SORTER_VINCULO_RETORNO_ETQ v
				 WHERE v.ID_GPT_RETORNO = r.ID_GPT_RETORNO_MONTAGEM_PALETES
			       )
			GROUP BY r.PALLET
		       HAVING COUNT(*) = :4
		     ) px'
	      INTO V_CNT_PALLET_CAND
	      USING V_NROEMPRESA, V_NROCARGA, V_SEQPRODUTO, V_QTD_PEND_DEST;
	  EXCEPTION
	    WHEN OTHERS THEN
	      V_CNT_PALLET_CAND := 0;
	  END;
	  CD2.DD_PKG_FECHA_CACHE.SET_CNT_PALLET(V_CACHE_CNT_KEY, V_CNT_PALLET_CAND);
	END IF;
	IF V_CNT_PALLET_CAND = 1 THEN
	  BEGIN
	    EXECUTE IMMEDIATE '
	      SELECT PALLET, COD_PRODUTIVO, DATA_ABERTURA, DATA_FECHAMENTO
		FROM (
		  SELECT r.PALLET,
				 MIN(r.COD_PRODUTIVO) AS COD_PRODUTIVO,
				 MIN(r.DATA_ABERTURA) AS DATA_ABERTURA,
				 MAX(r.DATA_FECHAMENTO) AS DATA_FECHAMENTO
			    FROM "GPT_RETORNO_MONTAGEM_PALETES"@INVENT r
			   WHERE r.EMPRESA = :1
			     AND r.ONDA = :2
			     AND r.SEQPRODUTO = :3
			     AND r.LOJA = r.EMPRESA
			     AND r.DATA_FECHAMENTO IS NOT NULL
			     AND NOT EXISTS (
				   SELECT 1
				     FROM CD2.DD_SORTER_VINCULO_RETORNO_ETQ v
				    WHERE v.ID_GPT_RETORNO = r.ID_GPT_RETORNO_MONTAGEM_PALETES
				  )
			   GROUP BY r.PALLET
			  HAVING COUNT(*) = :4
			  ORDER BY MAX(r.DATA_FECHAMENTO) DESC
		)
	       WHERE ROWNUM = 1'
	      INTO V_PALLET_INVENT, V_CODPROD_INVENT, V_DTA_ABERTURA, V_DTA_FECHAMENTO
	      USING V_NROEMPRESA, V_NROCARGA, V_SEQPRODUTO, V_QTD_PEND_DEST;
	  EXCEPTION
	    WHEN OTHERS THEN
	      V_PALLET_INVENT := NULL;
	  END;
	END IF;
      END IF;
    ELSIF V_PALLET_INVENT IS NOT NULL THEN
      BEGIN
	EXECUTE IMMEDIATE '
	  SELECT MIN(r.COD_PRODUTIVO), MIN(r.DATA_ABERTURA), MAX(r.DATA_FECHAMENTO)
	    FROM "GPT_RETORNO_MONTAGEM_PALETES"@INVENT r
	   WHERE r.EMPRESA = :1
	     AND r.ONDA = :2
	     AND r.SEQPRODUTO = :3
	     AND r.PALLET = :4'
	  INTO V_CODPROD_INVENT, V_DTA_ABERTURA, V_DTA_FECHAMENTO
	  USING V_NROEMPRESA, V_NROCARGA, V_SEQPRODUTO, V_PALLET_INVENT;
      EXCEPTION
	WHEN OTHERS THEN
	  NULL;
      END;
    END IF;
    /* Master obrigatoriamente coerente com retorno Invent deste SEQPRODUTO. */
    IF V_PALLET_INVENT IS NOT NULL AND V_SEQPRODUTO IS NOT NULL THEN
      BEGIN
	EXECUTE IMMEDIATE '
	  SELECT COUNT(*)
	    FROM "GPT_RETORNO_MONTAGEM_PALETES"@INVENT r
	   WHERE r.EMPRESA = :1
	     AND r.ONDA = :2
	     AND r.SEQPRODUTO = :3
	     AND r.PALLET = :4
	     AND r.DATA_FECHAMENTO IS NOT NULL'
	  INTO V_TMP_COUNT
	  USING V_NROEMPRESA, V_NROCARGA, V_SEQPRODUTO, V_PALLET_INVENT;
	IF V_TMP_COUNT = 0 THEN
	  V_PALLET_INVENT := NULL;
	  V_SEQPALETE := NULL;
	END IF;
      EXCEPTION
	WHEN OTHERS THEN
	  NULL;
      END;
    END IF;
    IF REGEXP_LIKE(NVL(V_PALLET_INVENT, ''), '^[Mm][0-9]+$') THEN
      V_SEQPALETE := TO_NUMBER(SUBSTR(V_PALLET_INVENT, 2));
    ELSIF REGEXP_LIKE(NVL(V_PALLET_INVENT, ''), '^[0-9]+$') THEN
      V_SEQPALETE := TO_NUMBER(V_PALLET_INVENT);
    ELSE
      V_SEQPALETE := NULL;
    END IF;
    /* Volume na C5: sufixo numerico da etiqueta (7 digitos no formato atual, fallback 6). */
    IF REGEXP_LIKE(P_CODBARRAETQ, '[0-9]{7}$') THEN
      V_SEQPALETEQTDE_DER := TO_NUMBER(REGEXP_SUBSTR(P_CODBARRAETQ, '[0-9]{7}$'));
    ELSIF REGEXP_LIKE(P_CODBARRAETQ, '[0-9]{6}$') THEN
      V_SEQPALETEQTDE_DER := TO_NUMBER(REGEXP_SUBSTR(P_CODBARRAETQ, '[0-9]{6}$'));
    ELSE
      V_SEQPALETEQTDE_DER := NULL;
    END IF;
    BEGIN
      IF V_SEQPALETEQTDE_DER IS NOT NULL THEN
	SELECT pq.SEQPALETERF
	  INTO V_SEQPALETERF_DER
	  FROM CONSINCO.MLO_PALETE p
	  JOIN CONSINCO.MLO_PALETEQTDE pq ON pq.SEQPALETERF = p.SEQPALETERF
	 WHERE p.NROEMPRESA = V_NROEMPRESA
	   AND p.NROCARGARECEB = V_NROCARGA
	   AND p.SEQPRODUTO = V_SEQPRODUTO
	   AND p.TIPESPECIE = V_TIPESPECIE
	   AND pq.SEQPALETEQTDE = V_SEQPALETEQTDE_DER
	   AND ROWNUM = 1;
      ELSE
	SELECT MAX(p.SEQPALETERF)
	  INTO V_SEQPALETERF_DER
	  FROM CONSINCO.MLO_PALETE p
	 WHERE p.NROEMPRESA = V_NROEMPRESA
	   AND p.NROCARGARECEB = V_NROCARGA
	   AND p.SEQPRODUTO = V_SEQPRODUTO
	   AND p.TIPESPECIE = V_TIPESPECIE;
      END IF;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
	V_SEQPALETERF_DER := NULL;
      WHEN OTHERS THEN
	V_SEQPALETERF_DER := NULL;
    END;
    V_SEQPALETE_VOLUME := NULL;
    IF V_SEQPALETERF_DER IS NOT NULL AND V_SEQPALETEQTDE_DER IS NOT NULL THEN
      BEGIN
	SELECT prf.SEQPALETECARREG
	  INTO V_SEQPALETE_VOLUME
	  FROM CONSINCO.MLO_PALCARREGRF prf
	 WHERE prf.SEQPALETERF = V_SEQPALETERF_DER
	   AND prf.SEQPALETEQTDE = V_SEQPALETEQTDE_DER
	   AND prf.SEQPALETECARREG IS NOT NULL
	   AND ROWNUM = 1;
      EXCEPTION
	WHEN NO_DATA_FOUND THEN
	  V_SEQPALETE_VOLUME := NULL;
	WHEN OTHERS THEN
	  V_SEQPALETE_VOLUME := NULL;
      END;
    END IF;
    BEGIN
      SELECT v.ID_GPT_RETORNO, v.SEQPALETECARREG, v.PALLET_INVENT
	INTO V_ID_GPT_RETORNO, V_SEQPALETE, V_PALLET_INVENT
	FROM CD2.DD_SORTER_VINCULO_RETORNO_ETQ v
       WHERE v.CODBARRAETQ = P_CODBARRAETQ;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
	NULL;
    END;
    IF V_SEQPALETERF_DER IS NOT NULL AND V_PALLET_INVENT IS NOT NULL THEN
      IF V_ID_GPT_RETORNO IS NULL THEN
	BEGIN
	  EXECUTE IMMEDIATE '
	    SELECT MIN(r2.ID_GPT_RETORNO_MONTAGEM_PALETES),
		   MIN(r2.PALLET) KEEP (DENSE_RANK FIRST ORDER BY r2.ID_GPT_RETORNO_MONTAGEM_PALETES)
	      FROM "GPT_RETORNO_MONTAGEM_PALETES"@INVENT r2
	     WHERE r2.EMPRESA = :1
	       AND r2.ONDA = :2
	       AND r2.SEQPRODUTO = :3
	       AND r2.PALLET = :4
	       AND r2.DATA_FECHAMENTO IS NOT NULL
	       AND (
		     (r2.LOJA = :5 AND r2.LOJA <> r2.EMPRESA)
		  OR (r2.LOJA = r2.EMPRESA)
		   )
	       AND NOT EXISTS (
		     SELECT 1
		       FROM CD2.DD_SORTER_VINCULO_RETORNO_ETQ v
		      WHERE v.ID_GPT_RETORNO = r2.ID_GPT_RETORNO_MONTAGEM_PALETES
		   )'
	    INTO V_ID_GPT_RETORNO, V_PALLET_INVENT
	    USING V_NROEMPRESA, V_NROCARGA, V_SEQPRODUTO, V_PALLET_INVENT, V_LOJA_DESTINO;
	EXCEPTION
	  WHEN OTHERS THEN
	    V_ID_GPT_RETORNO := NULL;
	END;
      END IF;
      BEGIN
	SELECT 1
	  INTO V_TMP_COUNT
	  FROM CD2.DD_SORTER_VINCULO_RETORNO_ETQ v
	 WHERE v.CODBARRAETQ = P_CODBARRAETQ;
      EXCEPTION
	WHEN NO_DATA_FOUND THEN
	  IF V_ID_GPT_RETORNO IS NOT NULL THEN
	    BEGIN
	      INSERT INTO CD2.DD_SORTER_VINCULO_RETORNO_ETQ (
		ID_VINCULO, CODBARRAETQ, ID_GPT_RETORNO, NROCARGA, NROEMPRESA,
		SEQPRODUTO, SEQPALETECARREG, PALLET_INVENT, DTA_VINCULO
	      ) VALUES (
		CD2.SQ_DD_SORTER_VINCULO_RETORNO_ETQ.NEXTVAL,
		P_CODBARRAETQ,
		V_ID_GPT_RETORNO,
		V_NROCARGA,
		V_NROEMPRESA,
		V_SEQPRODUTO,
		CASE
		  WHEN REGEXP_LIKE(NVL(V_PALLET_INVENT, ''), '^[Mm][0-9]+$')
		    THEN TO_NUMBER(SUBSTR(V_PALLET_INVENT, 2))
		  WHEN REGEXP_LIKE(NVL(V_PALLET_INVENT, ''), '^[0-9]+$')
		    THEN TO_NUMBER(V_PALLET_INVENT)
		  ELSE NULL
		END,
		V_PALLET_INVENT,
		SYSDATE
	      );
	    EXCEPTION
	      WHEN DUP_VAL_ON_INDEX THEN
		NULL;
	      WHEN OTHERS THEN
		V_ID_GPT_RETORNO := NULL;
	    END;
	  END IF;
      END;
    END IF;
    IF V_SEQPALETE_VOLUME IS NOT NULL THEN
      V_SEQPALETE := V_SEQPALETE_VOLUME;
    ELSIF V_SEQPALETE IS NULL AND V_PALLET_INVENT IS NOT NULL THEN
      IF REGEXP_LIKE(NVL(V_PALLET_INVENT, ''), '^[Mm][0-9]+$') THEN
	V_SEQPALETE := TO_NUMBER(SUBSTR(V_PALLET_INVENT, 2));
      ELSIF REGEXP_LIKE(NVL(V_PALLET_INVENT, ''), '^[0-9]+$') THEN
	V_SEQPALETE := TO_NUMBER(V_PALLET_INVENT);
      END IF;
    END IF;
    IF V_SEQPALETE IS NULL THEN
      BEGIN
	SELECT MAX(e.STATUS_ORIGEM) KEEP (DENSE_RANK LAST ORDER BY e.ID_EVENTO)
	  INTO V_ULT_FECHA_LOG
	  FROM CD2.DD_SORTER_EVENTO_CTRL e
	 WHERE e.CODBARRAETQ = P_CODBARRAETQ
	   AND e.ORIGEM = 'FECHAMENTO';
      EXCEPTION
	WHEN NO_DATA_FOUND THEN
	  V_ULT_FECHA_LOG := NULL;
      END;
      IF NVL(V_ULT_FECHA_LOG, 'X') <> 'AGUARDANDO_MONTAGEM' THEN
	BEGIN
	  INSERT INTO CD2.DD_SORTER_EVENTO_CTRL (
	    ID_EVENTO, CODBARRAETQ, ORIGEM, STATUS_ORIGEM, NROCARGA, NROEMPRESA,
	    DTA_EVENTO_ORIGEM, DTA_CAPTURA, HASH_EVENTO, OBS
	  ) VALUES (
	    CD2.SQ_DD_SORTER_EVENTO_CTRL.NEXTVAL,
	    P_CODBARRAETQ,
	    'FECHAMENTO',
	    'AGUARDANDO_MONTAGEM',
	    V_NROCARGA,
	    V_NROEMPRESA,
	    SYSDATE,
	    SYSDATE,
	    P_CODBARRAETQ || '|FECHA|AGUARD_MONT|' || V_TIPESPECIE,
	    CASE
	      WHEN V_SEQPALETERF_DER IS NULL THEN
